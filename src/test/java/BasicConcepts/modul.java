package BasicConcepts;

public class modul {
    public static void main(String[] args) {
        int value = 23;
        int res = value % 6;

        System.out.println(res);
    }
}

/*
       Деление 23 на 6 возвращает частное равное 3, с остатком равным 5.
       Таким образом, значение 5 присваивается переменной res.
 */
